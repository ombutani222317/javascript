// switch case:>
{
    let hero = "alu arjun";
    switch (hero) {
        case "salman khan":
            console.log("salaman khan is my fav.. hero");
            break;
        case "alu arjun":
            console.log("alu arjun  is my fav.. hero");
            break;
        case "akshay kumar":
            console.log("akshay kumar is my fav.. hero");
            break;
        default:
            break;
    }
}


