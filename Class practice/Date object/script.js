// Date object
// 1.Date get object
// 2.Date set object


// 1.Date get object 

{
    let date = new Date();
    date.getDate();
    date.getDay();
    date.getFullYear();
    date.getHours();
    date.getMilliseconds();
    date.getMinutes();
    date.getMonth();
    date.getSeconds();
    date.getTime();

    console.log(date);

}


// 2.Date set object 

{
    let date = new Date();
    date.setDate();
    date.setFullYear();
    date.setHours();
    date.setMilliseconds();
    date.setMinutes();
    date.setMonth();
    date.setSeconds();
    date.setTime();

    console.log(date);
}